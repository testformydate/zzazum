package com.mydate.zzazum.location.vo;

public class CourseVo {
	private int c_no;
	private String c_title, c_content, c_maker, c_like;
	
	public int getC_no() {
		return c_no;
	}
	public void setC_no(int c_no) {
		this.c_no = c_no;
	}
	public String getC_title() {
		return c_title;
	}
	public void setC_title(String c_title) {
		this.c_title = c_title;
	}
	public String getC_content() {
		return c_content;
	}
	public void setC_content(String c_content) {
		this.c_content = c_content;
	}
	public String getC_maker() {
		return c_maker;
	}
	public void setC_maker(String c_maker) {
		this.c_maker = c_maker;
	}
	public String getC_like() {
		return c_like;
	}
	public void setC_like(String c_like) {
		this.c_like = c_like;
	}
}
