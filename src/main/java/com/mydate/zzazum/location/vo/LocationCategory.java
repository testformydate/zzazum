public class LocationCategory {
	private int lc_no;
	private String lc_name, lc_val;
	
	
	public String getLc_val() {
		return lc_val;
	}
	public void setLc_val(String lc_val) {
		this.lc_val = lc_val;
	}
	public int getLc_no() {
		return lc_no;
	}
	public void setLc_no(int lc_no) {
		this.lc_no = lc_no;
	}
	public String getLc_name() {
		return lc_name;
	}
	public void setLc_name(String lc_name) {
		this.lc_name = lc_name;
	}
}
